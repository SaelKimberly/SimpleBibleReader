    enum Kind
    {
        IntArray,
        String,
    }

    class Book
    {
        int[] intArrayValue;
        string stringValue;

        Kind kind;

        public object Value
        {
            get
            {
                switch (kind)
                {
                    case Kind.IntArray:
                        return intArrayValue;
                    case Kind.String:
                        return stringValue;
                    default:
                        return null;
                }

            }
        }

        public override string ToString()
        {
            return Value.ToString();
        }

        // Implicit construction
        public static implicit operator Book(int[] i)
        {
            return new Book { intArrayValue = i, kind = Kind.IntArray };
        }
        public static implicit operator Book(string s)
        {
            return new Book { stringValue = s, kind = Kind.String };
        }

        // Implicit value reference
        public static implicit operator int[](Book Book)
        {
            if (Book.kind != Kind.IntArray) // Optionally, you could validate the usage
            {
                throw new InvalidCastException("Trying to use a " + Book.kind + " as an int[]");
            }
            return Book.intArrayValue;
        }
        public static implicit operator string(Book Book)
        {
            return Book.stringValue;
        }
    }
